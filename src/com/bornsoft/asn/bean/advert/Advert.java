package com.bornsoft.asn.bean.advert;

import java.util.Date;

import com.bornsoft.asn.utils.common.AsnConstLib.advertHandleStatus;
import com.bornsoft.asn.utils.common.AsnConstLib.advertStatus;

public class Advert {
	private Long advertId;
	private String advName;
	private String advType;
	private String advAddress;//广告位置
	private String advSpec;//规格
	private String description;
	private String picPath;//图片路径
	private Float quote; //价格
	private Date startTime;
	private Date endTime;
	private advertStatus status;	//广告状态，N－未发布；P－发布；C－撤销；S－正常关闭
	private advertHandleStatus handleStatus; //办理状态，N－未申请；A－申请中；P－申请成功；S－已签订合同；
	private Date createTime;
	private String createName;
	private proposerAdvert proposerAdvert;
	
	public proposerAdvert getProposerAdvert() {
		return proposerAdvert;
	}
	public void setProposerAdvert(proposerAdvert proposerAdvert) {
		this.proposerAdvert = proposerAdvert;
	}
	public Long getAdvertId() {
		return advertId;
	}
	public void setAdvertId(Long advertId) {
		this.advertId = advertId;
	}
	public String getAdvName() {
		return advName;
	}
	public void setAdvName(String advName) {
		this.advName = advName;
	}
	public String getAdvType() {
		return advType;
	}
	public void setAdvType(String advType) {
		this.advType = advType;
	}
	public String getAdvAddress() {
		return advAddress;
	}
	public void setAdvAddress(String advAddress) {
		this.advAddress = advAddress;
	}
	public String getAdvSpec() {
		return advSpec;
	}
	public void setAdvSpec(String advSpec) {
		this.advSpec = advSpec;
	}
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getPicPath() {
		return picPath;
	}
	public void setPicPath(String picPath) {
		this.picPath = picPath;
	}
	
	public Float getQuote() {
		return quote;
	}
	public void setQuote(Float quote) {
		this.quote = quote;
	}
	public Date getStartTime() {
		return startTime;
	}
	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}
	public Date getEndTime() {
		return endTime;
	}
	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}
	
	public advertStatus getStatus() {
		return status;
	}
	public void setStatus(advertStatus status) {
		this.status = status;
	}
	public advertHandleStatus getHandleStatus() {
		return handleStatus;
	}
	public void setHandleStatus(advertHandleStatus handleStatus) {
		this.handleStatus = handleStatus;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public String getCreateName() {
		return createName;
	}
	public void setCreateName(String createName) {
		this.createName = createName;
	}
	
	
}
