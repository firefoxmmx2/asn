package com.bornsoft.asn.bean.advert;

import java.util.Date;

import com.bornsoft.asn.bean.member.Member;
import com.bornsoft.asn.utils.common.AsnConstLib.proposerAdvertHandleStatus;

public class proposerAdvert {
	
	private Long proposerId;
	private Long advertId;
	private Long memberId;
	private Date applyTime;
	private proposerAdvertHandleStatus handleStatus; //A－申请中；P－审核通过；S－已签合同；U－审核未过；
	private Date handleTime;
	private String picPath;
	private String contractNumber; //合同编号
	private Date SignTime;
	private Member member;
	
	
	public Member getMember() {
		return member;
	}
	public void setMember(Member member) {
		this.member = member;
	}
	public Long getProposerId() {
		return proposerId;
	}
	public void setProposerId(Long proposerId) {
		this.proposerId = proposerId;
	}
	public Long getAdvertId() {
		return advertId;
	}
	public void setAdvertId(Long advertId) {
		this.advertId = advertId;
	}
	public Long getMemberId() {
		return memberId;
	}
	public void setMemberId(Long memberId) {
		this.memberId = memberId;
	}
	public Date getApplyTime() {
		return applyTime;
	}
	public void setApplyTime(Date applyTime) {
		this.applyTime = applyTime;
	}
	public proposerAdvertHandleStatus getHandleStatus() {
		return handleStatus;
	}
	public void setHandleStatus(proposerAdvertHandleStatus handleStatus) {
		this.handleStatus = handleStatus;
	}
	
	public Date getHandleTime() {
		return handleTime;
	}
	public void setHandleTime(Date handleTime) {
		this.handleTime = handleTime;
	}
	public String getPicPath() {
		return picPath;
	}
	public void setPicPath(String picPath) {
		this.picPath = picPath;
	}
	public String getContractNumber() {
		return contractNumber;
	}
	public void setContractNumber(String contractNumber) {
		this.contractNumber = contractNumber;
	}
	public Date getSignTime() {
		return SignTime;
	}
	public void setSignTime(Date signTime) {
		SignTime = signTime;
	}
}
