package com.bornsoft.asn.bean.infoOrder;

import java.io.Serializable;

public class InfoOrderType implements Serializable{
	private Long id;
	private String typeName;
	private String typeLevel;
	private String amount;
	private String subscribeIco;
	private String unsubscribeIco;
	private Integer indexNo;
	public InfoOrderType() {
		super();
	}
	public InfoOrderType(Long id, String typeName, String typeLevel,
			String amount, String subscribeIco, String unsubscribeIco,
			Integer indexNo) {
		super();
		this.id = id;
		this.typeName = typeName;
		this.typeLevel = typeLevel;
		this.amount = amount;
		this.subscribeIco = subscribeIco;
		this.unsubscribeIco = unsubscribeIco;
		this.indexNo = indexNo;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	public String getTypeLevel() {
		return typeLevel;
	}
	public void setTypeLevel(String typeLevel) {
		this.typeLevel = typeLevel;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getSubscribeIco() {
		return subscribeIco;
	}
	public void setSubscribeIco(String subscribeIco) {
		this.subscribeIco = subscribeIco;
	}
	public String getUnsubscribeIco() {
		return unsubscribeIco;
	}
	public void setUnsubscribeIco(String unsubscribeIco) {
		this.unsubscribeIco = unsubscribeIco;
	}
	public Integer getIndexNo() {
		return indexNo;
	}
	public void setIndexNo(Integer indexNo) {
		this.indexNo = indexNo;
	}
	
}
