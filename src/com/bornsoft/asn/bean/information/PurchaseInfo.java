package com.bornsoft.asn.bean.information;

import java.io.Serializable;
import java.util.Date;

public class PurchaseInfo extends Info implements Serializable {
	private Integer memberId;
	
	public PurchaseInfo() {
		super();
	}

	public PurchaseInfo(Integer infoId, String title, String type,
			String typeName, String infoStatus, String keyWord,
			String infoValidity, String detail, Date startDate,
			Date modifyDate, Date endDate, Date delDate, String ipAddreess,
			String area,Integer memberId,Integer censor) {
		super(infoId, title, type, typeName, infoStatus, keyWord, infoValidity, detail,
				startDate, modifyDate, endDate, delDate, ipAddreess, area,censor);
		this.memberId = memberId;
	}





	public Integer getMemberId() {
		return memberId;
	}

	public void setMemberId(Integer memberId) {
		this.memberId = memberId;
	}
}
