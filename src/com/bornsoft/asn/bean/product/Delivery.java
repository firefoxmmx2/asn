package com.bornsoft.asn.bean.product;

import java.io.Serializable;

public class Delivery implements Serializable {
	private Integer deliveryId;
	private Integer productId;
	private Double post;
	private Double express;
	private Double ems;
	private String postArea;
	
	public Delivery() {
		super();
	}

	public Delivery(Integer deliveryId, Integer productId, Double post,
			Double express, Double ems, String postArea) {
		super();
		this.deliveryId = deliveryId;
		this.productId = productId;
		this.post = post;
		this.express = express;
		this.ems = ems;
		this.postArea = postArea;
	}

	public Integer getDeliveryId() {
		return deliveryId;
	}

	public void setDeliveryId(Integer deliveryId) {
		this.deliveryId = deliveryId;
	}

	public Integer getProductId() {
		return productId;
	}

	public void setProductId(Integer productId) {
		this.productId = productId;
	}

	public Double getPost() {
		return post;
	}

	public void setPost(Double post) {
		this.post = post;
	}

	public Double getExpress() {
		return express;
	}

	public void setExpress(Double express) {
		this.express = express;
	}

	public Double getEms() {
		return ems;
	}

	public void setEms(Double ems) {
		this.ems = ems;
	}

	public String getPostArea() {
		return postArea;
	}

	public void setPostArea(String postArea) {
		this.postArea = postArea;
	}

	
	
	
}
