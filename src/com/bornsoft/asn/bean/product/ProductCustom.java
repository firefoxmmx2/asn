package com.bornsoft.asn.bean.product;

import java.io.Serializable;

public class ProductCustom implements Serializable {
	private Integer productCustomId;
	private Integer productId;
	private String propertyName;
	private String propertyDetail;
	
	public ProductCustom() {
		super();
	}

	public ProductCustom(Integer productCustomId, Integer productId,
			String propertyName, String propertyDetail) {
		super();
		this.productCustomId = productCustomId;
		this.productId = productId;
		this.propertyName = propertyName;
		this.propertyDetail = propertyDetail;
	}

	public Integer getProductCustomId() {
		return productCustomId;
	}

	public void setProductCustomId(Integer productCustomId) {
		this.productCustomId = productCustomId;
	}

	public Integer getProductId() {
		return productId;
	}

	public void setProductId(Integer productId) {
		this.productId = productId;
	}

	public String getPropertyName() {
		return propertyName;
	}

	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}

	public String getPropertyDetail() {
		return propertyDetail;
	}

	public void setPropertyDetail(String propertyDetail) {
		this.propertyDetail = propertyDetail;
	}
	
	

}
