package com.bornsoft.asn.bean.recruitment;

import java.io.Serializable;

public class Edu implements Serializable{
	private Long id;
	private Long employId;
	private String eduTimes;//教育时间
	private String schoolName;
	private String discripline;//专业
	private String educationalBackground;//学历
	private String discriplineDescription;//专业描述
	private Boolean studyAbroad;//海外学习经历
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getEmployId() {
		return employId;
	}
	public void setEmployId(Long employId) {
		this.employId = employId;
	}
	public String getEduTimes() {
		return eduTimes;
	}
	public void setEduTimes(String eduTimes) {
		this.eduTimes = eduTimes;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getDiscripline() {
		return discripline;
	}
	public void setDiscripline(String discripline) {
		this.discripline = discripline;
	}
	public String getEducationalBackground() {
		return educationalBackground;
	}
	public void setEducationalBackground(String educationalBackground) {
		this.educationalBackground = educationalBackground;
	}
	public String getDiscriplineDescription() {
		return discriplineDescription;
	}
	public void setDiscriplineDescription(String discriplineDescription) {
		this.discriplineDescription = discriplineDescription;
	}
	public Boolean getStudyAbroad() {
		return studyAbroad;
	}
	public void setStudyAbroad(Boolean studyAbroad) {
		this.studyAbroad = studyAbroad;
	}
	
	
}
