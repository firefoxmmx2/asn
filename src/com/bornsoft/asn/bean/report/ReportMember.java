package com.bornsoft.asn.bean.report;

import java.io.Serializable;

public class ReportMember implements Serializable{
	private static final long serialVersionUID = 6744151020370327277L;
    private String area;
    private String memberType; //会员类别
	private int countMember; //会员数量
    public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getMemberType() {
		return memberType;
	}
	public void setMemberType(String memberType) {
		this.memberType = memberType;
	}
	public int getCountMember() {
		return countMember;
	}
	public void setCountMember(int countMember) {
		this.countMember = countMember;
	}
}
