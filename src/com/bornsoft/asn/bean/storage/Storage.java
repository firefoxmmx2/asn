package com.bornsoft.asn.bean.storage;

import java.io.Serializable;
import java.util.Date;

public class Storage implements Serializable {
	private Integer productId;
	private Integer stockAmount;
	private String unit;
	private Integer alarmUp;
	private Integer alarmDown;
	private String status;
	private Date modifyTime;
	private Long modifyMan;
	public Integer getProductId() {
		return productId;
	}
	public void setProductId(Integer productId) {
		this.productId = productId;
	}
	public Integer getStockAmount() {
		return stockAmount;
	}
	public void setStockAmount(Integer stockAmount) {
		this.stockAmount = stockAmount;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public Integer getAlarmUp() {
		return alarmUp;
	}
	public void setAlarmUp(Integer alarmUp) {
		this.alarmUp = alarmUp;
	}
	public Integer getAlarmDown() {
		return alarmDown;
	}
	public void setAlarmDown(Integer alarmDown) {
		this.alarmDown = alarmDown;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Date getModifyTime() {
		return modifyTime;
	}
	public void setModifyTime(Date modifyTime) {
		this.modifyTime = modifyTime;
	}
	public Long getModifyMan() {
		return modifyMan;
	}
	public void setModifyMan(Long modifyMan) {
		this.modifyMan = modifyMan;
	}
	
	
	
}
