package com.bornsoft.asn.vo;

import java.io.Serializable;

public class ProductStorageDetail implements Serializable {
	private Long id;
	private Integer productId;
	private String smallPicPath;
	private String productName;
	private Long storageInOutId;
	private Integer billAmount;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Integer getProductId() {
		return productId;
	}
	public void setProductId(Integer productId) {
		this.productId = productId;
	}
	public String getSmallPicPath() {
		return smallPicPath;
	}
	public void setSmallPicPath(String smallPicPath) {
		this.smallPicPath = smallPicPath;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public Long getStorageInOutId() {
		return storageInOutId;
	}
	public void setStorageInOutId(Long storageInOutId) {
		this.storageInOutId = storageInOutId;
	}
	public Integer getBillAmount() {
		return billAmount;
	}
	public void setBillAmount(Integer billAmount) {
		this.billAmount = billAmount;
	}
}
